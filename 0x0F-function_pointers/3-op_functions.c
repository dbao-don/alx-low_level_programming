#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - a function that returns the
 * addition of two integers
 * @a: first integer
 * @b: second integer
 * Return: value of the operation
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - a function that returns the
 * subtration of two integers
 * @a: first integer
 * @b: second integer
 * Return: value of the operation
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - a function that returns the
 * multiplication of two integers
 * @a: first integer
 * @b: second integer
 * Return: value of the operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - a function that returns the
 * division of two integers
 * @a: first integer
 * @b: second integer
 * Return: value of the operation
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a function that returns the
 * modulus of two integers
 * @a: first integer
 * @b: second integer
 * Return: value of the operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
